<H2 previous="realization" next="failingToBeStraight">Mental Foundation</H2>
<P>
	One summer evening after my new self-realization, I remember helping serve dinner and then
	carrying everyone's dirty dishes away. My sister joked "Ac is being so nice lately. He must have,
	like, gotten a girl pregnant and is just being super nice before you guys find out about it." My
	parents laughed while I cringed. I remember thinking, <i
		>I wish I had feelings like that for a girl. It would be so much better than the feelings I am
		really experiencing</i
	>.
</P>
<P>
	In addition to doubting what my attraction to boys meant, I was beginning to feel more distant
	from my father. Most of the things he seemed to enjoy doing, such as fixing cars, I found no
	interest in. Instead, I was more likely to play with my sister's dolls or don a dress she gave me
	to amuse myself. My dad didn't know how to reach me during that time probably because he lacked a
	father figure in his own life.
</P>
<P>
	To intensify the mental conflict, I began to encounter unanimously negative messages about “the
	gay people.” Whether in person or online, gossip and slander formed my first impression of my
	sexuality. Thus, I quickly grew accustomed to disgusted or fearful reactions around the topic.
</P>
<P>
	One evening, my mom called me to my parents' bedroom. As soon as I saw my father's face, I knew I
	was in trouble. "What is this?" he pointed to his laptop. Logged in to my Gmail, he was browsing
	through some of my more explicit emails with Mark. I stumbled over my words as guilt swallowed me.
	I don't recall getting any sort of sex-ed from my parents, but my environment at least taught me
	not to talk dirty with friends. My father pointed to more emails. Between my apologies and mental
	gymnastics to try to extricate myself from an incredibly uncomfortable situation, I claimed that
	it meant nothing and that the emails were just jokes and bad ideas.
</P>
<P>
	I can't recall if my parents were crying, but I know I was. It was emotional, but I didn't try too
	hard to hold it in since I hoped it would help alleviate my punishment. I left scolding myself for
	being careless enough to engage in prohibited activities where my parents might see them. I aimed
	to be more careful next time.
</P>
<Break />
<P>
	Very soon, based on some of the things the Bible said about "homosexuals" and the general
	atmosphere about the topic at school, I began to descend into much self-doubt and mental conflict
	over the feelings I was having.
</P>
<P>
	My most vivid memory that came to symbolize how the church viewed me took place in seventh grade.
	One Sabbath I was working in the brand-new audio-visual booth above a church congregation, when
	prior to his sermon, the pastor announced, "Now, brothers and sisters, I've heard of other
	churches in California doing some dangerous things that contradict the word of God. You may have
	heard of churches that accept homosexuals. I just wanted to say that we are not going to allow
	ourselves to follow in their footsteps! We are God's people. No homosexuals will be accepted in <b
		>this</b
	> church!"
</P>
<P>
	The amens resounded. To their credit, I never recall a full "anti-homosexual sermon" from any of
	the pastors throughout my years at that church. However, the words spoken from the pulpit that
	morning skewered my heart. I slumped down in my chair in front of several soundboards and
	trembled. Although the aging congregation appreciated my pastor's promise not to compromise their
	beliefs and invite "those people" into our church, they failed to realize that I was <b>already</b
	> there.
</P>
<P>
	By the time I was thirteen, it seemed like the anti-homosexual narrative was everywhere. It is
	unlikely that something in my environment had drastically changed, but I became hyper-aware of
	anything that could provide information about the feelings I was experiencing. When my principal
	discussed with my teacher how he thought that homosexuals were just confused, I started listening.
	When my teacher theorized, "What they are feeling isn't love. They don't know what love is. I
	mean, how could they?" the listening stopped. A habit of self-derision and overthinking settled in
	its place.
</P>
<P>
	The adults in my life seldom offered any type of understanding toward those with whom I shared my
	feelings as I became a teenager. Because being gay was considered so disgusting in my community,
	the topic was never discussed in public. This formed a progressively lonelier existence for me as
	I didn't know of anyone I could relate to. Even worse, I was now involuntarily part of a group of
	people that many in my community feared or hated.
</P>
<Break />
<P>
	Until seventh grade, I still probably could have come to more logical conclusions about myself and
	my sexuality. Instead, an experience near the end of elementary school embedded fear, hatred, and
	self-preservation into my understanding of sexuality.
</P>
<P>
	Once I started having significant desires to find others with whom I could relate, I managed to
	join a dating app through Facebook and control my preferences so that I could message other gay
	teens. I began to secretly borrow one of my mom's devices during the night to talk to a guy named
	Tristan, who, of course, assured me that he loved me and wanted to spend the rest of his life with
	me. I wasn't gullible enough to believe everything he told me, but hearing "I love you more" or
	"Good morning handsome" was a welcome change from my daily reality where anyone I might have liked
	had no capacity to like me back. Also, recognizing that I wasn't the only one experiencing
	attraction to the same gender gave me the necessary space to relax the walls I had built up over
	the past few years.
</P>
<P>
	Having access to the internet in the middle of the night eventually led to some more Pornhub
	exploration, but both that and my secret correspondence promptly ended a week or two later when I
	fell asleep instead of returning the device one night.
</P>
<Break />
<P>
	"<b>SMACK!</b>" I woke up on a Wednesday morning while my head was mid-flight between my father's
	palm and the side of my bunk bed. Another blow hit my face before I realized what was happening.
	"When you come back from school, I'm going to deal with you." I quickly collected my thoughts as
	my father's words faded.
</P>
<P>
	During the entire school day and the subsequent extracurricular activities, I found myself worried
	and racked with nerves. What didn't make sense was that my dad had hit me. Hard. My father had
	never been physically abusive. I knew it was often a problem in other households, but I believed I
	was lucky enough to not have to deal with that. This aberration clearly conveyed that he was
	serious. Every moment of that day was filled with anxiety about what might happen when I saw him
	again.
</P>
<P>
	At home, instead of helping my mom unload groceries, I was directed to follow my father into the
	woods behind our home. I was still mentally unprepared to process the situation. Shocked, I took
	more punches and slaps without resistance.
</P>
<P>
	Soon, he took me to an empty room in our basement and delivered the heartfelt lecture that I had
	initially expected. I recall how he admitted that sometimes guys "have a lot of testosterone" and
	they may need to "let it out." However, <b>this</b> (gay sexual activity) was what had disgusted him
	the most ever since he was a boy. After half an hour, the message I received was clear. Being gay was
	the most disgusting thing. I felt like I would have rather been in trouble for actually having sex
	with a girl than for watching gay porn.
</P>
<P class="conversation md:leading-loose">
	"You know that this is the worst thing you could be doing." "Mhmmm." "You want to have a wife and
	kids someday, right?" "Yeah, sure. " "Mommy and I talked, and we decided that we are going to find
	professional help for you so you can figure out these confused feelings." "Okay." "And don't tell
	anyone else about this. Especially don't talk to Grandma and Grandpa about this. I'm not sure if
	they could take it at their age."
</P>
<P>
	I wasn't used to seeing my father cry, but back then, I thought I understood why this occasion was
	so significant to him. I tried to imagine what it would be like to realize that my own child
	embodied what I found the most distasteful. Realistically, his motives were likely more grounded
	in what he believed was best for me, but what I remembered most was yet another adult's disgust at
	my sexuality. My father apologized the next day, but I wasn't nearly ready to forgive him.
	Instead, I mentally followed a path that further isolated me. Because my parents were not native
	English speakers, I was not accustomed to receiving academic help. This carried over to emotional
	struggles that were never really discussed at home. Although in time they would have likely
	listened if I was willing to share, I wasn't about to start opening up when I faced something as
	scary as having feelings for guys.
</P>
<P>
	Thus, I aimed instead to distance myself from my parents to emotionally protect myself from their
	reaction to my sexuality. I set out to stop caring about what my parents, especially my father,
	thought about me. This strategy proved to be almost too successful. I can't say that anything my
	parents did or said after that made a difference to me regarding my sexuality.
</P>
<Break />
<P>
	My parents booked me a weekly meeting with a therapist at a private therapy center in a nearby
	city. I had read stories online about conversion therapy camps and some of the more conservative
	Exodus-affiliated ministries and was justifiably horrified when I heard I was going to see a
	therapist that specialized in cases like mine. Thankfully my experience was nothing like a story
	out of <i>Boy Erased</i> or <i>Stranger at the Gate</i>. Instead of electric shock therapy or
	verses from Leviticus in picture frames, I was met with several shelves lined with books about
	marriage.
</P>
<P>
	I admit that I wasn't an easy 13-year-old to work with, but in my defense, I had my doubts about
	the bald man with a million questions. One of the success stories he boasted was about a gay man
	who had come to him six or seven years prior and had finally purged himself of all sexual
	attractions. Now he wasn't attracted to anyone at all. If that wasn't a worthy goal, what was,
	right? I silently wished for that but decided not to let it show.
</P>
<P>
	In the first of my few meetings with him, I was informed that I was not actually homosexually
	oriented, but rather I was going through a phase. I went in and out of therapy every Tuesday with
	the same thick book on <i>A Godly Marriage</i> and the same determination to quit as soon as I could.
	I didn't want help from someone who only researched sexuality. I wanted help from someone who could
	relate to what I was going through, but my therapist admitted he was incapable of that.
</P>
<P>
	Unfortunately, some of my friends in junior high also supported my growing self-hatred. Comments
	among the pre-teens often attacked any form of same-sex attraction. Many of my friends would cat
	call each other and slap each other on the crotch. As far as I knew I was the only gay one, but I
	was also often the only one uncomfortable with that behavior. Very soon, my early introduction to
	and interest in sexual activity inverted when it was made abundantly clear that attraction to the
	same gender was just about the dirtiest thing that you could experience.
</P>
<P>
	Since I was being taken out of school every Tuesday for therapy, it took less than a month for my
	then best friend and first crush in eighth grade, Jack, to figure out what was going on. First, he
	started theorizing that I was going to get help with some mental health issue, then specifically
	that it was for sexual orientation therapy. Having someone safe to talk to would have been amazing
	at this point, but instead, one of my closest friends since kindergarten turned on me.
</P>
<P>
	It began with teasing, lewd jokes, and name calling. Soon, he avoided me altogether. I retaliated
	by trying to be nice to him every time he reminded me how disgusting I was. Miraculously, this
	either confused him or wore away his resolve to hate me, because the following school year he
	stopped bothering me. Sadly, this would not be the only friend I would lose over the issue of my
	sexuality.
</P>
<Break />
<P>
	The message I received from my environment was unanimous. Being attracted to other boys was a sin.
	Most destructive was the follow-up message: it was worse than any other sin. There were probably
	church members who would have been caring and non-judgmental if I had come to them, but at this
	point I was too afraid to tell anyone. I had never heard the topic discussed in a civil manner,
	and the handful of adults that knew my secret unanimously advised me to bury it as deeply as I
	could. So, I did.
</P>
<P>
	The problem that most fundamentally scarred me at this age was others' ability to nonchalantly
	dehumanize queer individuals. The first form of dehumanization was ignorance. Many of the people I
	knew would rather pretend that I and others in the queer community didn't exist. When sexuality
	was discussed, most assumed that no one around was gay. Thus, language such as “those people”,
	“the homosexuals”, and “fags” was often used.
</P>
<P class="conversation">
	"I just can't stand it when <i>they</i> hold hands in public." "What has our world come to. Now we
	have to watch our backs, so <i>those homosexual pedophiles</i> don't hurt our kids."
</P>
<P>
	Secondly, the distaste towards homosexuality was extremely widespread. For example, I vividly
	remember driving through our small, southern town the day after Obergefell v. Hodges was decided.
	Several church signs proudly advertised sermon titles such as "Two Men? That's Not a Marriage" and
	"It's Adam and Eve, not Adam and Steve."
</P>
<P>
	All these messages pounding me with shame from many directions caused my understanding of my own
	sexuality to deteriorate abysmally. My many prayers from that time best describe my feelings. One
	night, I became especially worried about my increasing interest in boys. I decided to pray for
	deliverance once again, but instead I found myself pleading for a compromise.
</P>
<P class="conversation">
	"Dear God, you saw me struggling with my thoughts today. I don't know why, but something is wrong
	with me. I've tried so hard not to think about guys, but I can't seem to get any better. I know
	you didn't make heaven for people like me, and I know that I'm not going there, but that's okay.
	All I ask is that even though I belong in hell, if you could please use me to help get other
	people to be in heaven, that could make my time worthwhile on Earth. That would make me happy.
	Amen."
</P>

